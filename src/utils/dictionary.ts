// 获取单词中文翻译
export const getChineseTranslation = async (word: string): Promise<string | null> => {
  const cleanWord = word.replace(/[^a-zA-Z]/g, '').toLowerCase();

  // 先检查本地映射
  if (commonWordsCN[cleanWord]) {
    return commonWordsCN[cleanWord];
  }

  // 尝试从中文词典API获取
  try {
    const response = await fetch(`${CN_DICTIONARY_API}?word=${cleanWord}`);
    if (response.ok) {
      const data = await response.json();
      return data.translation || null;
    }
  } catch (error) {
    // 静默失败，使用英文翻译
  }

  return null;
};

// 获取单词释义
export const fetchWordDefinition = async (word: string): Promise<WordDefinition | null> => {
  try {
    const cleanWord = word.replace(/[^a-zA-Z]/g, '').toLowerCase();
    if (!cleanWord) return null;

    const response = await fetch(`${DICTIONARY_API}/${cleanWord}`);

    if (!response.ok) {
      return null;
    }

    const data = await response.json();
    return data[0] as WordDefinition;
  } catch (error) {
    console.error('Error fetching word definition:', error);
    return null;
  }
};

// 使用Web Speech API播放发音
export const playPronunciation = (word: string, audioUrl?: string): void => {
  // 如果有音频文件URL，优先使用
  if (audioUrl) {
    const audio = new Audio(audioUrl);
    audio.play().catch(console.error);
    return;
  }

  // 否则使用Web Speech API
  if ('speechSynthesis' in window) {
    const utterance = new SpeechSynthesisUtterance(word);
    utterance.lang = 'en-US';
    utterance.rate = 0.9;
    utterance.pitch = 1;
    window.speechSynthesis.speak(utterance);
  }
};

// 清理单词（去除标点符号）
export const cleanWord = (word: string): string => {
  return word.replace(/[^a-zA-Z]/g, '').toLowerCase();
};

// 检查是否为有效单词
export const isValidWord = (word: string): boolean => {
  const cleaned = cleanWord(word);
  return cleaned.length > 0 && cleaned.length <= 50;
};
